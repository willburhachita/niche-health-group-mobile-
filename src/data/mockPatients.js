export function getPatientById(id) {
  return mockPatients.find(p => p.id === id);
}

export function getActivePatients() {
  return mockPatients.filter(p => p.status === 'active');
}

export function searchPatients(query) {
  const q = query.toLowerCase();
  return mockPatients.filter(p =>
    p.displayName.toLowerCase().includes(q) ||
    p.patientId.toLowerCase().includes(q) ||
    p.phone.includes(q)
  );
}

export function getPatientsByDepartment(dept) {
  return mockPatients.filter(p => p.department === dept);
}

export function getRecentPatients(count = 3) {
  return [...mockPatients]
    .filter(p => p.status === 'active')
    .sort((a, b) => b.lastVisit - a.lastVisit)
    .slice(0, count);
}
